static kal_uint16 read_cmos_sensor(kal_uint32 addr)
{
    kal_uint16 get_byte= 0;
    char pusendcmd[2] = {(char)(addr >> 8) , (char)(addr & 0xFF) };
    iReadRegI2C(pusendcmd , 2, (u8*)&get_byte, 2, imgsensor.i2c_write_id);
    return ((get_byte<<8)&0xff00)|((get_byte>>8)&0x00ff);
}


static void write_cmos_sensor(kal_uint16 addr, kal_uint16 para)
{
    char pusendcmd[4] = {(char)(addr >> 8) , (char)(addr & 0xFF) ,(char)(para >> 8),(char)(para & 0xFF)};
    iWriteRegI2C(pusendcmd , 4, imgsensor.i2c_write_id);
}

static kal_uint16 read_cmos_sensor_8(kal_uint16 addr)
{
    kal_uint16 get_byte= 0;
    char pusendcmd[2] = {(char)(addr >> 8) , (char)(addr & 0xFF) };
    iReadRegI2C(pusendcmd , 2, (u8*)&get_byte,1,imgsensor.i2c_write_id);
    return get_byte;
}

static void write_cmos_sensor_8(kal_uint16 addr, kal_uint8 para)
{
    char pusendcmd[4] = {(char)(addr >> 8) , (char)(addr & 0xFF) ,(char)(para & 0xFF)};
    iWriteRegI2C(pusendcmd , 3, imgsensor.i2c_write_id);
}

#define MULTI_WRITE 1

#if MULTI_WRITE
#define I2C_BUFFER_LEN 1020	/* trans# max is 255, each 3 bytes */
#else
#define I2C_BUFFER_LEN 4

#endif

static kal_uint16 table_write_cmos_sensor(kal_uint16 *para, kal_uint32 len)
{
	char puSendCmd[I2C_BUFFER_LEN];
	kal_uint32 tosend, IDX;
	kal_uint16 addr = 0, addr_last = 0, data;

	tosend = 0;
	IDX = 0;
	while (len > IDX) {
		addr = para[IDX];

		{
			puSendCmd[tosend++] = (char)(addr >> 8);
			puSendCmd[tosend++] = (char)(addr & 0xFF);
			data = para[IDX + 1];
			puSendCmd[tosend++] = (char)(data >> 8);
			puSendCmd[tosend++] = (char)(data & 0xFF);
			IDX += 2;
			addr_last = addr;

		}
#if MULTI_WRITE
		/* Write when remain buffer size is less than 4 bytes or reach end of data */
		if ((I2C_BUFFER_LEN - tosend) < 4 || IDX == len || addr != addr_last) {
			iBurstWriteReg_multi(puSendCmd, tosend, imgsensor.i2c_write_id,
								4, imgsensor_info.i2c_speed);
			tosend = 0;
		}
#else
		iWriteRegI2CTiming(puSendCmd, 4, imgsensor.i2c_write_id, imgsensor_info.i2c_speed);
		tosend = 0;

#endif
	}
	return 0;
}



static void set_dummy(void)
{
    LOG_INF("dummyline = %d, dummypixels = %d \n", imgsensor.dummy_line, imgsensor.dummy_pixel);
    write_cmos_sensor(0x0340, imgsensor.frame_length);
    write_cmos_sensor(0x0342, imgsensor.line_length);
}	/*	set_dummy  */


static void set_max_framerate(UINT16 framerate,kal_bool min_framelength_en)
{

    kal_uint32 frame_length = imgsensor.frame_length;

    LOG_INF("framerate = %d, min framelength should enable %d \n", framerate,min_framelength_en);

    frame_length = imgsensor.pclk / framerate * 10 / imgsensor.line_length;
    spin_lock(&imgsensor_drv_lock);
    if (frame_length >= imgsensor.min_frame_length)
        imgsensor.frame_length = frame_length;
    else
        imgsensor.frame_length = imgsensor.min_frame_length;
    imgsensor.dummy_line = imgsensor.frame_length - imgsensor.min_frame_length;

    if (imgsensor.frame_length > imgsensor_info.max_frame_length)
    {
        imgsensor.frame_length = imgsensor_info.max_frame_length;
        imgsensor.dummy_line = imgsensor.frame_length - imgsensor.min_frame_length;
    }
    if (min_framelength_en)
        imgsensor.min_frame_length = imgsensor.frame_length;
    spin_unlock(&imgsensor_drv_lock);
    set_dummy();
}	/*	set_max_framerate  */

static kal_uint32 streaming_control(kal_bool enable)
{
	int timeout = (10000 / imgsensor.current_fps) + 1;
	int i = 0;
	int framecnt = 0;

	LOG_INF("streaming_enable(0= Sw Standby,1= streaming): %d\n", enable);
	if (enable) {
		write_cmos_sensor_8(0x0100, 0X01);
		mdelay(10);
	} else {
		write_cmos_sensor_8(0x0100, 0x00);
		for (i = 0; i < timeout; i++) {
			mdelay(5);
			framecnt = read_cmos_sensor_8(0x0005);
			if ( framecnt == 0xFF) {
				LOG_INF(" Stream Off OK at i=%d.\n", i);
				return ERROR_NONE;
			}
		}
		LOG_INF("Stream Off Fail! framecnt= %d.\n", framecnt);
	}
	return ERROR_NONE;
}


static void write_shutter(kal_uint32 shutter)
{

	kal_uint16 realtime_fps = 0;
	int framecnt = 0;
	LOG_INF("===hope\n");
	if(shutter > 65530) {  //linetime=10160/960000000<< maxshutter=3023622-line=32s
		/*enter long exposure mode */
		kal_uint32 exposure_time;
		kal_uint16 new_framelength;
		kal_uint16 long_shutter;
		kal_uint16 shift = 0;
		kal_uint16 pow_shift = 0;
		LOG_INF("enter long exposure mode\n");
		LOG_INF("Calc long exposure  +\n");
		
		bIsLongExposure = KAL_TRUE;
		
		LOG_INF("enter long exposure mode shutter = %d, imgsensor.line_length =%d\n", shutter, imgsensor.line_length);
		//exposure_time = shutter*imgsensor_info.cap.linelength/960000;//ms
		
		/*exposure_time = shutter/1000*imgsensor.line_length/960;ms*/
		exposure_time = shutter/1000*imgsensor.line_length/960;
		/*exposure_time = shutter*1000*imgsensor.line_length/imgsensor.pclk;ms*/
		
		if(exposure_time <= 1000) {
			shift = 0x0100;
			pow_shift = 2;
		} else if (exposure_time > 1000 && exposure_time <= 2000) {
			shift = 0x0200;
			pow_shift = 4;
		} else if (exposure_time > 2000 && exposure_time <= 4000) {
			shift = 0x0300;
			pow_shift = 8;
		} else if (exposure_time > 4000 && exposure_time <= 8000) {
			shift = 0x0400;
			pow_shift = 16;
		} else if (exposure_time > 8000 && exposure_time <= 16000) {
			shift = 0x0500;
			pow_shift = 32;
		} else if (exposure_time > 16000) {
			shift = 0x0600;
			pow_shift = 64;
		}

		//long_shutter = (exposure_time*pclk-256)/lineleght/64 = (shutter*linelength-256)/linelength/pow_shift
        long_shutter = shutter/pow_shift;
		new_framelength = long_shutter+7;
        LOG_INF("Calc long exposure_time=%dms,pow_shift = %d, long_shutter=%d, new_framelength=%d, imgsensor.line_length = %d, shift = %d.\n", exposure_time, pow_shift, long_shutter, new_framelength, imgsensor.line_length, shift);
		LOG_INF("Calc long exposure  -\n");

		/*streaming_control(KAL_FALSE);*/
		write_cmos_sensor(0x6028, 0x4000);
		write_cmos_sensor_8(0x0100, 0x00); /*stream off*/
		while (1) {
			//mdelay(2);
			//write_cmos_sensor_8(0x0100, 0x00); /*stream off*/
			mdelay(5);
			framecnt = read_cmos_sensor_8(0x0005);
			LOG_INF(" Stream Off oning at framecnt=0x%x.\n", framecnt);
			if ( framecnt == 0xFF) {
				LOG_INF(" Stream Off OK at framecnt=0x%x.\n", framecnt);
				break;
			}
		}
		write_cmos_sensor(0x6028, 0x2000);
		write_cmos_sensor(0x602A, 0x2D1E);
		write_cmos_sensor(0x6F12, 0x8000);
		write_cmos_sensor(0x602A, 0x2D22);
		write_cmos_sensor(0x6F12, 0x0310);
		write_cmos_sensor(0x602A, 0x379E);
		write_cmos_sensor(0x6F12, 0x000D);

		write_cmos_sensor(0x0342, imgsensor.line_length);
		write_cmos_sensor(0x0340, new_framelength);
		write_cmos_sensor(0x0202, long_shutter);
		write_cmos_sensor(0x0702, shift);
		write_cmos_sensor(0x0704, shift);
		/*streaming_control(KAL_TRUE);*/
		
		write_cmos_sensor_8(0x0100, 0x01);  /*stream on*/
		/* Frame exposure mode customization for LE*/
		imgsensor.ae_frm_mode.frame_mode_1 = IMGSENSOR_AE_MODE_SE;
		imgsensor.ae_frm_mode.frame_mode_2 = IMGSENSOR_AE_MODE_SE;
		imgsensor.current_ae_effective_frame = 1;
		
		LOG_INF("long exposure  stream on-\n");
	} else {
		imgsensor.current_ae_effective_frame = 1;
		if (bIsLongExposure) {
		LOG_INF("enter normal shutter.\n");
		write_cmos_sensor(0x6028, 0x4000);
		write_cmos_sensor_8(0x0100, 0x00); /*stream off*/
		while (1) {
			//mdelay(2);
			//write_cmos_sensor_8(0x0100, 0x00); /*stream off*/
			mdelay(5);
			framecnt = read_cmos_sensor_8(0x0005);
			LOG_INF(" Stream Off oning at framecnt=0x%x.\n", framecnt);
			if ( framecnt == 0xFF) {
				LOG_INF(" Stream Off OK at framecnt=0x%x.\n", framecnt);
				break;
			}
		}
		write_cmos_sensor(0x6028, 0x2000);
		write_cmos_sensor(0x602A, 0x2D1E);
		write_cmos_sensor(0x6F12, 0x8000);
		write_cmos_sensor(0x602A, 0x2D22);
		write_cmos_sensor(0x6F12, 0x0310);
		write_cmos_sensor(0x602A, 0x379E);
		write_cmos_sensor(0x6F12, 0x000D);
		
		write_cmos_sensor(0x6028, 0x4000);
		write_cmos_sensor(0x0342, imgsensor.line_length);
		write_cmos_sensor(0x0340, imgsensor.frame_length);
		write_cmos_sensor(0x0202, 0x0100);
		write_cmos_sensor(0x0702, 0x0000);
		write_cmos_sensor(0x0704, 0x0000);
		
		write_cmos_sensor_8(0x0100, 0x01);  /*stream on*/
		bIsLongExposure = KAL_FALSE;
		LOG_INF("===hope enter normal shutter shutter = %d, imgsensor.frame_lengths = 0x%x, imgsensor.line_length = 0x%x\n", shutter,imgsensor.frame_length, imgsensor.line_length);
		}
	spin_lock(&imgsensor_drv_lock);
	if (shutter > imgsensor.min_frame_length - imgsensor_info.margin)
		imgsensor.frame_length = shutter + imgsensor_info.margin;
	else
		imgsensor.frame_length = imgsensor.min_frame_length;
	if (imgsensor.frame_length > imgsensor_info.max_frame_length)
		imgsensor.frame_length = imgsensor_info.max_frame_length;
	spin_unlock(&imgsensor_drv_lock);
	if (shutter < imgsensor_info.min_shutter) shutter = imgsensor_info.min_shutter;

	if (imgsensor.autoflicker_en) {
		realtime_fps = imgsensor.pclk / imgsensor.line_length * 10 / imgsensor.frame_length;
		if(realtime_fps >= 297 && realtime_fps <= 305)
			set_max_framerate(296,0);
		else if(realtime_fps >= 147 && realtime_fps <= 150)
			set_max_framerate(146,0);
		else {
		/* Extend frame length*/
	        write_cmos_sensor(0x0340, imgsensor.frame_length);

	    }
	} else {
		/* Extend frame length*/
		write_cmos_sensor(0x0340, imgsensor.frame_length);
	}
	
	/* Update Shutter*/
	    write_cmos_sensor(0x0202, shutter);
	LOG_INF("shutter = %d, framelength = %d\n", shutter,imgsensor.frame_length);
	}

}	/*	write_shutter  */



